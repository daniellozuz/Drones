from math import sin, cos, radians
from random import randint
from statistics import mean


def circle():
    n = 0
    for angle in range(0, 360, 18):
        x = round(10*sin(radians(angle)), 2)
        y = round(10*cos(radians(angle)), 2)
        n += 1
        print(n, x, y)


def wobbly_circle():
    n = 0
    for angle in range(0, 360, 9):
        x = round(randint(90, 100)*sin(radians(angle)), 2)
        y = round(randint(90, 100)*cos(radians(angle)), 2)
        n += 1
        print(n, x, y)


